		//2. Set 순서를 유지하지 않는 데이터의 집합. 데이터의 중복을 허용하지 않음. HashSet, TreeSet 등. ex) 정수의 집합, 소수의 집합
		//3. Map 키(key)와 값(value)의 쌍(pair)으로 이루어진 데이터의 집합. 순서는 유지되지 않으며, 키는 중복을 허용하지 않고, 값은 중복을 허용. HashMap, TreeMap, Hashtable, Properties등. ex) 우편번호, 전화번호

